    NumeroSala VARCHAR(10) NOT NULL,
    DisciplinaID CHAR(6) NOT NULL,
    CursoID CHAR(8) NOT NULL,
    FOREIGN KEY (DisciplinaID, CursoID) REFERENCES DISCIPLINA(DisciplinaID, CursoID)
);

